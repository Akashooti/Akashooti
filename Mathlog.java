import java.io.*;
public class MathLog
{
    public static void main(String[] args)
    {
double x = 24;
double log = Math.log(x);
System.out.println("Log value is = " + log);
double log10 = Math.log10(x);
System.out.println("Log10 value is = " + log10);
double log1p = Math.log1p(x);
System.out.println("Log1p value is = " + log1p);
}
}
